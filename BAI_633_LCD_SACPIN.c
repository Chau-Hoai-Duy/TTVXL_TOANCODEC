#DEFINE BOARD D501
#include<tv_boards.c>
#include<tv_lcd.c>
const unsigned int8 cucpin[]={0x04,0x1F,0x11,0x11,0x11,0x11,0x11,0x1F,                                          
0x04,0x1F,0x11,0x11,0x11,0x11,0x1F,0x1F,                                          
0x04,0x1F,0x11,0x11,0x11,0x1F,0x1F,0x1F,                                          
0x04,0x1F,0x11,0x11,0x1F,0x1F,0x1F,0x1F,                                          
0x04,0x1F,0x11,0x1F,0x1F,0x1F,0x1F,0x1F,                                          
0x04,0x1F,0x1F,0x1F,0x1F,0x1F,0x1F,0x1F                           
};
unsigned int8 n,i;
void main()
{
   system_init();
   lcd_setup();
   lcd_command(0x40);
   for(n=0;n<48;n++) lcd_data(cucpin[n]);
   lcd_goto(0,0);
   lcd_data("DANG SAC PIN");
   while(true)
   {
      for(i=0;i<6;i++)
      {
         lcd_goto(19,0);
         lcd_data(i);
         delay_ms(500);
      }
   }
}
